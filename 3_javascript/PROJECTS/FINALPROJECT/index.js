function makecard(data){

  const root = document.getElementById("root");

    data.forEach((restaurant)=>{

        // CARD CREATED
        const card = document.createElement('div');
        card.classList.add("card");

        // IMAGE + OFFER
        const image = document.createElement("div");
        image.classList.add("image");


        const img = document.createElement("img");
        img.classList.add("img");
        img.src = `./images/${restaurant.image}.avif`

        const p = document.createElement("p");
        p.classList.add("off");
        p.innerHTML = `Flat ${restaurant.offers}% OFF`;

        
        
        
        // RATINGS
        const rest = document.createElement("div");
        rest.classList.add("restaurant");
        
        const rest_name = document.createElement("div");
        rest_name.classList.add("restaurant_name");
        rest_name.classList.add("box");

        const p3 = document.createElement("p");
        p3.innerHTML = restaurant.restaurant;

        const rat = document.createElement("div");
        rat.classList.add("rating");
        rat.classList.add("box");

        const p4 = document.createElement("p");
        p4.id = 'mr';
        p4.innerHTML = restaurant.rating;

        const star = document.createElement("div");
        star.id = "star";
        star.innerHTML = `&#9733;`;
        
        
        
        
        // FOOD + PRICE
        const food = document.createElement("div");
        food.classList.add("food");
        
        const food_name = document.createElement("div");
        food_name.classList.add("food_name");
        food_name.classList.add("box");

        const p2 = document.createElement("p");
        p2.innerHTML = `Food type - ${restaurant.food_type}`;

        const price = document.createElement("div");
        price.classList.add("price");
        price.classList.add("box");
        
        const p6 = document.createElement("p");
        p6.innerHTML = `₹${restaurant.price_for_two} for two`;

        
        // LOCATION
        const location = document.createElement("div");
        location.classList.add("location");
        
        const place = document.createElement("div");
        place.classList.add("place");
        place.classList.add("box");
        
        const p5 = document.createElement("p");
        p5.innerHTML = `Location - ${restaurant.location}`;

        const distance = document.createElement("div");
        distance.classList.add("distance");
        distance.classList.add("box");
        distance.innerHTML = `${restaurant.distance}Km`;
        
        image.appendChild(img);
        image.appendChild(p);
        rest_name.appendChild(p3);
        rat.appendChild(p4);
        p4.appendChild(star);

        rest.appendChild(rest_name);
        rest.appendChild(rat);
        
        food_name.appendChild(p2);
        price.appendChild(p6);
        food.appendChild(food_name);
        food.appendChild(price);

        place.appendChild(p5);
        location.appendChild(place);
        location.appendChild(distance);

        card.appendChild(image);
        card.appendChild(rest);
        card.appendChild(food);
        card.appendChild(location);


        root.appendChild(card);



    });

}

