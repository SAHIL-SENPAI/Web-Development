document.getElementById("applyfilter").addEventListener('click',()=>{

  console.log("clicked")


    const element = document.querySelector('input[name="filteroperation"]:checked');
    const answer = element.value;

    console.log(answer)

    if(answer==='rating'){
      bulkdata.sort((a,b)=>b.rating - a.rating);
    }
    else if (answer === "highlow"){

      bulkdata.sort((a,b)=>b.price_for_two-a.price_for_two);
    }
    else if (answer === "costlowhigh"){

      bulkdata.sort((a,b)=>a.price_for_two-b.price_for_two);
    }
    else if (answer === "distance"){
      bulkdata.sort((a,b)=>a.distance-b.distance);
    }


    document.getElementById('root').replaceChildren();
    makecard(bulkdata)

    
    document.getElementById("popup").classList.add("hidden");
    document.getElementById("overlay").style.display = 'none';



})

